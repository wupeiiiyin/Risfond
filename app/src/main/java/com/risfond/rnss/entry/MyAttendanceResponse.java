public class MyAttendanceResponse {

    /**我的考勤
     * Code : 0
     * Status : true
     * Message : 请求成功
     */

    private int Code;
    private boolean Status;
    private String Message;
    private List<MyAttendance> Data;

    public int getCode() {
        return Code;
    }

    public void setCode(int Code) {
        this.Code = Code;
    }

    public boolean isStatus() {
        return Status;
    }

    public void setStatus(boolean Status) {
        this.Status = Status;
    }

    public String getMessage() {
        return Message;
    }

    public void setMessage(String Message) {
        this.Message = Message;
    }

    public List<MyAttendance> getData() {
        return Data;
    }

    public void setData(List<MyAttendance> data) {
        Data = data;
    }
}
